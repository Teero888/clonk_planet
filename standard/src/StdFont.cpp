CStdFont::CStdFont() { Default(); }

CStdFont::~CStdFont() { Clear(); }

void CStdFont::Default() {
  Surface.Default();
  for (int cnt = 0; cnt < FNT_MaxChar; cnt++)
    Character[cnt].Default();
}

void CStdFont::Clear() {
  Surface.Clear();
  for (int cnt = 0; cnt < FNT_MaxChar; cnt++)
    Character[cnt].Clear();
}

